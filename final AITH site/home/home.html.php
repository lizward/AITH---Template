                <h2>About Ace in the Hole Multisport Events</h2>
                
                <p>Ace in the Hole Multisport Events is proud to offer running and triathlon events to athletes of all
                    shapes and sizes, national origins, sexual orientations and cultural backgrounds. We offer Events for Every Body.</p>

                <h2>About the Event</h2>

                <p>The Annual Ace in the Hole MultiSport Weekend is a legendary event in the Oregon triathlon and running community.
                    It has become a traditional destination race for athletes from across the nation.<br>
                    There is something for every level of athletic ability. The weekend includes a first timer triathlon, a sprint,
                    Olympic, and Half-Iron triathlons and 10K and Half marathon runs. Come to experience your first race or come to
                    compete to win, but make sure you come to have fun!
                </p>



