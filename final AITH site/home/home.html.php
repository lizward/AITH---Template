                <h2>Course Details</h2>
                <img class="image" src="../images/games-3436353_1920.jpg" alt="wheelchair race">
                <p>WATER TEMPERATURE is expected to be between 62 – 66 degrees. The temperature will be taken on Friday and the morning of the race. Wetsuits are recommended.</p>
                <h3>Long Course Triathlon</h3>
                <h4>Long Course SWIM – 1.2mi</h4>
                <p>Participants will make two counter-clockwise loops. Large buoys will mark the turn points. Kayakers will be positioned on the water to support the
                    swimmers. Medical support will be present on the beach.</p>
                <h4>Long Course BIKE – 58 Miles</h4>
                <p>A scenic point-to-point course that travels over gently rolling hills prior to three hard climbs. The bike course will be marked with large directional
                    signage and there will be course marshals at key intersections to help direct you. </p>
                <h4>Long Course RUN – 13.1mi</h4>
                <p>A mostly flat loop course on widely paved bike paths that traverse through and around this beautiful and scenic destination resort (two hills total with a
                    minimal elevation gain).</p>
                <h3>Olympic Triathlon</h3>
                <h4>OLYMPIC SWIM – 1,500 meters</h4>
                <p>Participants will make two counter-clockwise loops. Large buoys will mark the turn points. Kayakers will be positioned on the water to support the
                    swimmers. Medical support will be present on the beach.</p>
                <h4>OLYMPIC BIKE – 28mi</h4>
                <p>A scenic point-to-point course that travels over gently rolling hills. The bike course will be marked with large directional signage and course marshals
                    will be present at key intersections. </p>
                <h4>OLYMPIC RUN – 10K</h4>
                <p>A mostly flat loop course on widely paved bike paths that traverse through and around this beautiful and scenic destination resort (one hill total with
                    a minimal elevation gain).</p>
                <h3>Sprint</h3>
                <p>Course will offer a 1-loop 1/2 mile swim, the exact same 28 mile Bike Course as the Olympic distance and a 5km run.</p>
                <h3>Try-A-Tri</h3>
                <p>This novice race is designed for the first time triathlete, those new to the sport, our Junior Triathletes. The swim is a shorter, more manageable 1/4
                    mile distance, (instead of the standard 1/2-mile Sprint distance swim), 10 mile bike ride (vs 12 miles and it's a 2 loop course making it very spectator
                    friendly!) and a flat 2 mile run (vs 3 mile sprint course).</p>
                <h3>Half Marathon COURSE</h3>
                <p>Half-Marathon (13.1-miles): Half-Marathon event starts and finishes in the Athletes Village to the cheers of the enthusiastic crowd. Once finished,
                    runners can enjoy the finish line festivities, including the Sports & Fitness Expo and live entertainment. Post-race refreshments will be provided
                    and the Awards Ceremony for the Half-Marathon will begin once the results have been certified</p>
                <h3>10k COURSE</h3>
                <p>The 10K event starts and finishes in the Athletes Village. The paths are approximately 6 ft wide, perfectly paved and wind around through the forest.
                    Each course has only two small hills with a minimal elevation gain to navigate and a fast downhill to flat finish to the roaring cheers of the crowd.</p>
                <h3>Splash n Dash</h3>
                <p>An event for kids ages 6 to 14. It is a 100 yard swim and a 50 meter dash. The event is free if an adult registers for an event.</p>

